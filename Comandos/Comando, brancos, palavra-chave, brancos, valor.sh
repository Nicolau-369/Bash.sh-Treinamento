Arquivo: inputrc
Programa: Biblioteca Readline (Comportamento da linha de comando)
Detalhes: A linha de configuração é um comando (set). As palavras-chave
são separadas internamente pelo hífen e possuem valores padrão, não se
pode colocar qualquer valor. Utiliza on e off para ligar e desligar funções.
set bell-style visible
set meta-flag on
set convert-meta off
set output-meta on
set editing-mode vi
set completion-ignore-case on